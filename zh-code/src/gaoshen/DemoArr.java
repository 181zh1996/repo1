package gaoshen;
//动态初始化（指定长度）直接指定数组中的数据元素个数
// 格式  数据类型[] 数组名 = new 数据类型 [数组长度]
public class DemoArr {
    public static void main(String[] args) {
        //定义int类型 数组 100个数值
        int [] arr1 = new int[100];
        //定义double类型 数组 50个数值
        double [] arr2 = new double[50];
        //定义String类型 数组 10个数值
        String [] arr3 = new String[10];
        //标准格式拆分两个步骤
        int [] arr01;
        arr01 = new int[5];
    }
}
