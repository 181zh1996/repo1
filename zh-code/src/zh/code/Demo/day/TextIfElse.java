public class TextIfElse {
    public static void main(String[] args) {
        int num1=15;
        if (num1 %2 == 0){
            System.out.println(num1+"是偶数");
        }else {
            System.out.println(num1+"是奇数");
        }
        System.out.println("结束");
    }
}
